<script>
    $(document).ready(function() {
        showNewRequestNotification();
        sumTotalEarnings();
        countTotalTownAndDistricts();
        countTotalSS_SP_NewReq();

        // * Function for show new SP signup notification to admin while login
        function showNewRequestNotification() {
            $.ajax({
                url: '../ajax-file/ajax.php',
                type: 'POST',
                data: {
                    "request": "notifyNewRequest"
                },
                success: function(response) {
                    if (response > 0) {
                        response < 9 ? newNumberFormat = `0${response}` : newNumberFormat = response;

                        Swal.fire({
                            title: "New Service Provider Request",
                            text: `Dear Admin! You have ${newNumberFormat} pending request.`,
                            icon: "info"
                        }).then((result) => {
                            console.log(result);
                            if (result.isConfirmed == true) {
                                window.location.href = "/skill-wave-service-hiring-app/admin/dashboard.php?requests";
                            }
                        });
                    }
                },
                error: function(xhr, status, error) {
                    console.log("Status: " + status);
                    console.log("XHR Response: " + xhr.responseText);
                }
            })
        }

        // * Sum total earnings
        function sumTotalEarnings() {

            $.ajax({
                url: '../ajax-file/ajax.php',
                type: 'POST',
                data: {
                    "request": "sumAllServiceEarnings"
                },
                success: function(response) {
                    // console.log(response);
                    const jsonData = JSON.parse(response);
                    const {
                        totalEarnings
                    } = jsonData

                    $("#total-earnings")[0].textContent = `LKR. ${totalEarnings}.00`;
                },
                error: function(xhr, status, error) {
                    console.log("Status: " + status);
                    console.log("XHR Response: " + xhr.responseText);
                }
            })
        }

        // * Count total town & districts
        function countTotalTownAndDistricts() {
            $.ajax({

                url: '../ajax-file/ajax.php',
                type: 'POST',
                data: {
                    "request": "countTotalTownAndDistrict"
                },
                success: function(response) {

                    // console.log(response);
                    const jsonData = JSON.parse(response);
                    const {
                        totalTown,
                        total_district
                    } = jsonData[0];

                    totalTown < 9 ? town = `0${totalTown}` : town = totalTown;
                    total_district < 9 ? district = `0${total_district}` : district = total_district;

                    $("#count-district")[0].textContent = district;
                    $("#count-town")[0].textContent = town;
                },
                error: function(xhr, status, error) {
                    console.log("Status: " + status);
                    console.log("XHR Response: " + xhr.responseText);
                }
            })

        }

        //* Function for count Total SS, SP, New Req
        function countTotalSS_SP_NewReq() {
            $.ajax({

                url: '../ajax-file/ajax.php',
                type: 'POST',
                data: {
                    "request": "countAdminDashboardEls"
                },
                success: function(response) {
                    // console.log(response);
                    const jsonData = JSON.parse(response);
                    // console.log(jsonData[0]);
                    const {
                        total_requests,
                        total_sp,
                        total_ss
                    } = jsonData[0];

                    total_requests < 9 ? requests = `0${total_requests}` : requests = total_requests
                    total_sp < 9 ? sp = `0${total_sp}` : sp = total_sp
                    total_ss < 9 ? ss = `0${total_ss}` : ss = total_ss

                    $("#new-req")[0].textContent = requests;
                    $("#total-sp")[0].textContent = sp;
                    $("#total-ss")[0].textContent = ss;
                }

            })
        }
    })
